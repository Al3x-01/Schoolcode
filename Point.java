public class Point
{
    private double x;
    private double y;
    
    public Point(int xVal, int yVal)
    {
        x = xVal;
        y = yVal;
    }
    
    public double getX()
    {
        return x;
    }
    
    public double getY()
    {
        return y;
    }
    
    public String toString()
    {
        return "(" + x + ", " + y + ")";
    }
    public Point ()
    {
        
    }
    
    
}


